#pragma once

#include <string>

/// @file
/// @brief エンジン SDK のバッファ所有権ポリシー。
///
/// @note 依存は <std> のみ。サードパーティヘッダはここに含めない。本ヘッダは、
///       型エイリアスと規範ドキュメントとして、SDK の残りが従うメモリ/バッファ
///       ポリシーを規定する（docs/memory-buffer-policy.md および CLAUDE.md
///       "Engine live memory is never sent over the transport" を参照）。
namespace norves::bridge
{

    /// @brief OwnedFrame は自己所有のワイヤーペイロード。すなわち UTF-8 テキストとして
    ///        エンコードされた完全な制御チャネル JSON ドキュメントである。SDK は
    ///        OwnedFrame 値をスレッド / キュー / トランスポート境界を越えて move する
    ///        ことしかしない。
    ///
    /// 所有権規則（規範。SDK はこれらを守るように書かれており、組み込み側も従わなければ
    /// ならない）:
    ///
    ///   1. 所有する。決して借用しない。キューやトランスポートに渡されるものは
    ///      OwnedFrame（値所有の std::string）または値所有の JsonValue スナップショット
    ///      である。SDK はエンジンのライブメモリへの借用ビュー
    ///      （std::string_view / span / ポインタ）をエンキューしたり送信したりしない。
    ///
    ///   2. エンキュー時に所有権が移譲される。フレームをキューへ push() すると
    ///      呼び出し側のストレージがキューへ move される。呼び出し後、呼び出し側は
    ///      自身の move 済み値を空として扱わなければならない。pop()/wait_and_pop() は
    ///      所有権をコンシューマへ move して戻す。
    ///
    ///   3. キューは保持するものを所有し、解放する。オーバーフロー（ドロップ）時、
    ///      シャットダウン時、または破棄時に、キューはまだ保持しているフレームを解放する。
    ///      コンシューマが pop したものを除き、いかなるフレームもキューより長生きしない。
    ///
    ///   4. 境界を越える前にスナップショットを取る。エンジンのライブメモリは、キューや
    ///      トランスポートに到達する前に所有スナップショット（OwnedFrame / JsonValue）へ
    ///      変換される。ライブメモリは、そのスナップショットを生成した呼び出しを越えて
    ///      エイリアスされることは決してない。
    ///
    /// @note アロケータ / バッファプールフック: この層では意図的に存在しない。OwnedFrame は
    ///       std::string のデフォルトアロケータを使う。カスタムアロケータやバッファプールは
    ///       将来のオプション拡張である（cpp.md はアロケータ / バッファプールフックを
    ///       オプションとして保ち、未指定時はデフォルトアロケータを使う）。それが追加される
    ///       までは、SDK は完全にデフォルトアロケータ上で動作する。
    using OwnedFrame = std::string;

}  // namespace norves::bridge
